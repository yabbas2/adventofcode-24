def merge_sort(arr):
    if len(arr) <= 1:
        return arr

    mid = len(arr) // 2
    left_half = arr[:mid]
    right_half = arr[mid:]

    sorted_left = merge_sort(left_half)
    sorted_right = merge_sort(right_half)

    return merge(sorted_left, sorted_right)


def merge(left, right):
    result = []
    i = j = 0

    while i < len(left) and j < len(right):
        if left[i] < right[j]:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1

    result.extend(left[i:])
    result.extend(right[j:])

    return result


def get_total_distance(left, right):
    total_distance = 0

    assert len(left) == len(right), "ERROR: Lists with different length"
    for i in range(len(left)):
        total_distance += abs(left[i] - right[i])

    return total_distance


def binary_search(arr, val):
    left = 0
    right = len(arr) - 1

    while left <= right:
        mid = (left + right) // 2

        if arr[mid] == val:
            return mid

        if arr[mid] < val:
            left = mid + 1
        else:
            right = mid - 1

    return -1


count_map = {}


def get_binary_search_count(arr, val):
    if val in count_map:
        return count_map[val]

    idx = binary_search(arr, val)
    if idx == -1:  # not found
        count_map[val] = 0
        return 0
    # else: at least found once
    count = 1

    # check similar numbers around idx
    r_idx = idx + 1
    while r_idx < len(arr) and arr[r_idx] == val:
        count += 1
        r_idx += 1
    l_idx = idx - 1
    while l_idx >= 0 and arr[l_idx] == val:
        count += 1
        l_idx -= 1

    count_map[val] = count
    return count


def get_similarity_score(left, right):
    total_sim_score = 0

    for num in left:
        total_sim_score += num * get_binary_search_count(right, num)

    return total_sim_score


